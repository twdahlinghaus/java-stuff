package ui;

public class LaunchCountdownApp {

	public static void main(String[] args) {
		System.out.println("Welcome to the Launch Countdown App!!");
		System.out.println();
		System.out.println("We are ready for launch in T minus ");
		
		for (int count = 10; count >= 0; count--)
			System.out.println(count);
		
		System.out.println("All engines go!");
		System.out.println("We have lift off!!");
			
		
		System.out.println("Up, up and awaaaaaaay!!");

	}

}
